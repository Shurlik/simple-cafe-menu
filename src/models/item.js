class Item {
    constructor(title,
                index,
                price,
                weight,
                unit,
                description,
                category,
                hot,
                available,
                image) {

        this.title = title
        this.index = index
        this.price = price
        this.weight = weight
        this.unit = unit
        this.description = description
        this.category = category
        this.hot = hot
        this.available = available
        this.image = image
    }
}

export default Item;
